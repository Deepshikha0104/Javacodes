package operators;

public class arith {
    public static void main(String[] args) {
        var a = 10;
        var b = 5;
        var sum = a+b;
        var difference = a-b;
        var product = a*b;
        var quotient = a/b;
        System.out.println("Sum: " + sum);   // 15
        System.out.println("Difference: " + difference); // 5
        System.out.println("Product: " + product); // 50
        System.out.println("Quotient: " + quotient); // 2
    }
}
